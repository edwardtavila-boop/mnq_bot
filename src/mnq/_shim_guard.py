def heal_all_guarded_files(*, raise_on_failure: bool = False) -> dict[str, ShimHealth]:
    """Run the full self-heal sweep — shim plus every guarded file.

    Returns a mapping from filename → final health. Useful for logs and
    the conftest summary line.
    """
    out: dict[str, ShimHealth] = {}
    # Shim first — most critical, historically the flakiest.
    out["firm_runtime.py"] = ensure_firm_runtime_healthy(
        raise_on_failure=raise_on_failure,
    )
    for target, fix_name, required, required_returns, min_bytes in GUARDED_FILES:
        known_good = _find_fix(fix_name)
        out[target.name] = ensure_file_healthy(
            target,
            known_good=known_good,
            required_symbols=required,
            required_fn_returns=required_returns,
            min_bytes=min_bytes,
            raise_on_failure=raise_on_failure,
        )
    return out
