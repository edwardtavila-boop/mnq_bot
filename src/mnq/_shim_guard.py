# Generic guard — extends the self-healing pattern beyond firm_runtime.py.
#
# The OneDrive truncation artifact has now hit three files in three different
# batches:
#
#   - src/mnq/firm_runtime.py            (v5..v9, 5 times)
#   - src/mnq/eta_v3/__init__.py        (v9)
#   - src/mnq/eta_v3/meta_adapter.py    (v10, mid-apply_meta_overrides)
#
# The sync bug truncates at ~3.5KB. For each at-risk file we now keep a
# known-good copy in upload_bundle/firm-basement-audit/fixes/, and the
# root conftest calls ``heal_all_guarded_files()`` on every pytest startup.
# ---------------------------------------------------------------------------

_FIXES_DIR_CANDIDATES: tuple[Path, ...] = tuple(
    p
    for p in (
        # Parent of repo (/mnt/upload_bundle/...)
        _REPO_ROOT.parent / "upload_bundle" / "firm-basement-audit" / "fixes",
        # Two levels up — the session-root layout
        # (/sessions/<id>/upload_bundle/...). This is where the real
        # fixes live in the current workspace.
        (_REPO_ROOT.parents[1] if len(_REPO_ROOT.parents) >= 2 else _REPO_ROOT)
        / "upload_bundle"
        / "firm-basement-audit"
        / "fixes",
        # Sibling of src/ (in-tree upload bundle)
        _REPO_ROOT / "upload_bundle" / "firm-basement-audit" / "fixes",
    )
)


def _find_fix(filename: str) -> Path | None:
    """Locate ``<fixes_dir>/<filename>`` across candidate paths."""
    for base in _FIXES_DIR_CANDIDATES:
        p = base / filename
        if p.exists():
            return p
    return None


def _structural_check(
    tree: ast.Module,
    required_symbols: tuple[str, ...],
    required_fn_returns: tuple[str, ...] = (),
) -> str | None:
    """Return a failure reason string, or None if structure looks intact.

    A module is "structurally ok" if:

    - Each of ``required_symbols`` appears as a top-level function /
      async function / class / assignment / AnnAssign target / simple
      import alias.
    - Each of ``required_fn_returns`` names a top-level function whose
      body contains at least one ``Return`` node. This catches the
      truncation case where the function is defined but its body got
      lopped off before the return (the firm_runtime.py pattern).
    """
    defined: set[str] = set()
    fn_nodes: dict[str, ast.FunctionDef | ast.AsyncFunctionDef] = {}
    for node in tree.body:
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            defined.add(node.name)
            fn_nodes[node.name] = node
        elif isinstance(node, ast.ClassDef):
            defined.add(node.name)
        elif isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name):
                    defined.add(target.id)
                elif isinstance(target, (ast.Tuple, ast.List)):
                    for elt in target.elts:
                        if isinstance(elt, ast.Name):
                            defined.add(elt.id)
        elif isinstance(node, ast.AnnAssign) and isinstance(node.target, ast.Name):
            defined.add(node.target.id)
        elif isinstance(node, (ast.Import, ast.ImportFrom)):
            for alias in node.names:
                defined.add(alias.asname or alias.name.split(".")[0])

    missing = [s for s in required_symbols if s not in defined]
    if missing:
        return f"missing_symbols: {','.join(missing)}"

    for fn_name in required_fn_returns:
        fn = fn_nodes.get(fn_name)
        if fn is None:
            return f"missing_function: {fn_name}"
        has_return = any(isinstance(n, ast.Return) for n in ast.walk(fn))
        if not has_return:
            return f"truncated_no_return: {fn_name}"
        # Stronger check: the last top-level statement in the function
        # body must be a Return (or a compound statement that always
        # returns). This catches the case where the function has an
        # early guard return but the main flow got truncated — the
        # exact pattern OneDrive has hit on apply_meta_overrides twice.
        if fn.body:
            last = fn.body[-1]
            if not _ends_with_return(last):
                return f"truncated_body_missing_final_return: {fn_name}"

    return None


def _ends_with_return(node: ast.stmt) -> bool:
    """Does this statement guarantee a Return as its final outcome?

    Conservatively handles: bare Return, If with both branches ending
    in return, Try/With/While/For wrappers whose body ends in return.
    For the truncation case we care about, ``return out`` as the last
    line of the function — a straight ``Return`` node — is enough.
    """
    if isinstance(node, ast.Return):
        return True
    if isinstance(node, ast.If):
        return bool(node.body and _ends_with_return(node.body[-1])) and bool(
            node.orelse and _ends_with_return(node.orelse[-1])
        )
    if isinstance(node, (ast.With, ast.AsyncWith, ast.Try)):
        return bool(node.body and _ends_with_return(node.body[-1]))
    return False


def check_file_health(
    path: Path,
    *,
    required_symbols: tuple[str, ...] = (),
    required_fn_returns: tuple[str, ...] = (),
    min_bytes: int = 0,
) -> ShimHealth:
    """Generic sibling of ``check_shim_health`` for non-shim files.

    Parameters
    ----------
    path : Path
        File to inspect.
    required_symbols : tuple[str, ...]
        Module-level names that MUST exist (functions, classes,
        assignments, or imports). Useful for guarding ``__init__.py``
        re-export lists — if the file was truncated before the last
        import, we catch it here.
    min_bytes : int
        Optional floor on file size. Files under this threshold are
        flagged as ``too_small``; useful when we know a file should
        always be at least N bytes and a smaller version is a sync
        artifact.
    """
    if not path.exists():
        return ShimHealth(False, "missing", path, 0)

    raw = path.read_bytes()
    size = len(raw)
    if size == 0:
        return ShimHealth(False, "empty", path, 0)
    if size < min_bytes:
        return ShimHealth(False, f"too_small: {size} < {min_bytes}", path, size)

    try:
        tree = ast.parse(raw, filename=str(path))
    except SyntaxError as exc:
        return ShimHealth(False, f"syntax_error: {exc.msg}", path, size)

    reason = _structural_check(tree, required_symbols, required_fn_returns)
    if reason is not None:
        return ShimHealth(False, reason, path, size)

    return ShimHealth(True, "ok", path, size)


def ensure_file_healthy(
    path: Path,
    *,
    known_good: Path | None,
    required_symbols: tuple[str, ...] = (),
    required_fn_returns: tuple[str, ...] = (),
    min_bytes: int = 0,
    raise_on_failure: bool = True,
) -> ShimHealth:
    """Generic sibling of ``ensure_firm_runtime_healthy``.

    Uses ``check_file_health`` for the structural check, then falls back
    to the same atomic-write restore pattern.
    """
    initial = check_file_health(
        path,
        required_symbols=required_symbols,
        required_fn_returns=required_fn_returns,
        min_bytes=min_bytes,
    )
    if initial.ok:
        return initial

    if known_good is None or not known_good.exists():
        msg = (
            f"{path.name} is broken ({initial.reason}, "
            f"{initial.size_bytes} bytes) and no known-good copy is "
            f"available."
        )
        if raise_on_failure:
            raise RuntimeError(msg)
        logger.error(msg)
        return initial

    logger.warning(
        "%s is %s (%d bytes). Restoring from %s.",
        path,
        initial.reason,
        initial.size_bytes,
        known_good,
    )
    tmp = path.with_suffix(path.suffix + ".repair.tmp")
    shutil.copy2(known_good, tmp)
    tmp.replace(path)

    final = check_file_health(
        path,
        required_symbols=required_symbols,
        required_fn_returns=required_fn_returns,
        min_bytes=min_bytes,
    )
    if not final.ok:
        msg = (
            f"Restore of {path.name} from {known_good} produced an "
            f"unhealthy file: {final.reason}. The known-good copy may "
            f"itself be corrupt."
        )
        if raise_on_failure:
            raise RuntimeError(msg)
        logger.error(msg)
    else:
        logger.info("%s successfully restored.", path.name)
    return final


